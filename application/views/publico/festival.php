	<p>Formar parte del grupo de grandes Ferias de Videojuegos de América Latina.</p>
	<h2>Misión</h2>
	<p>Implementaremos año tras año un proyecto alineado con estándares internacionales en la producción de Ferias de Videojuegos garantizando la producción de eventos de muy alta calidad a lo largo de la región. Ofreceremos servicios integrados para nuestros expositores y auspiciadores así como la mejor experiencia posible para nuestros seguidores y visitantes en cada edición de la Feria AGS.</p>
